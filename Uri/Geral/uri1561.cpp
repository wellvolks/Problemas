#include <bits/stdc++.h>

using namespace std;

int main(){
	ios::sync_with_stdio(false);
	int h, m;
	char in[100];
	char ans[][50]={{" ____________________________________________\0"},
					{"|                                            |\0"},
					{"|    ____________________________________    |_\0"},
					{"|   |                                    |   |_)\0"},
					{"|   |   8         4         2         1  |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |                                    |   |\0"},
					{"|   |   32    16    8     4     2     1  |   |_\0"},
					{"|   |____________________________________|   |_)\0"},
					{"|                                            |\0"},
					{"|____________________________________________|\0"}};
	while( gets(in) != NULL ){
		h = (in[0]-'0')*10 + (in[1]-'0');
		m = (in[3]-'0')*10 + (in[4]-'0');
		int at = 38;
		for( int i = 0; i < 4; i++ ){
			if( h & (1 << i) ) ans[6][at] = 'o';
			at -= 10;
		}
		at = 38;
		for( int i = 0; i < 6; i++ ){
			if( m & (1 << i) ) ans[9][at] = 'o';
			at -= 6;
		}
		for( int i = 0; i < 15; i++ ) puts(ans[i]);
		puts("");
		at = 38;
		for( int i = 0; i < 4; i++ ){ ans[6][at] = ' '; at -= 10; }
		at = 38;
		for( int i = 0; i < 6; i++ ){ ans[9][at] = ' '; at -= 6; }
	}

	return 0;
}